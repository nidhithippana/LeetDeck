-- LeetDeck — first-sign-in trigger
-- Run this AFTER 002_rls.sql.
--
-- When a new user signs up via Supabase Auth, automatically create their
-- profiles row using metadata pulled from Google (name + avatar).
-- Means the app never has to write profiles on sign-up — one less failure mode.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (user_id, display_name, avatar_url)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'full_name', new.raw_user_meta_data->>'name', new.email),
    new.raw_user_meta_data->>'avatar_url'
  )
  on conflict (user_id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();
